# 练习:
#   写一个函数myadd, 此函数中的参数列表里有两个参数x, y
#   此函数的功能是打印两个实参的和(即 x + y的和)
#   如:
#     def myadd(...):
#         ...

#     myadd(100, 200)   # 打印300
#     myadd("ABC", "123")  # 打印 ABC123
  

def myadd(x, y):
    he = x + y
    print(he)

myadd(100, 200)   # 打印300
myadd("ABC", "123")  # 打印 ABC123
  
