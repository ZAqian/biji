# 练习:
#   任意输入一个整数,分三步做如下事情:
#     1) 判断这个数是否大于100
#     2) 判断这个数是否小于0
#     3) 判断这个数是否在50~150之间
  
x = int(input("请输入一个整数: "))
if x > 100:
    print(x, '大于100')
else:
    print(x, '不大于100')

if x < 0:
    print(x, '小于零')
else:
    print(x, '不小于零')

if 50 < x < 150:
    print(x, '在50~150之间')
else:
    print(x, '不在50~150之间')


