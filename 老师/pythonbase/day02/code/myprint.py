# myprint.py

# 此示例示意print函数的用法
print(1, 2, 3, 4)
print(1, 2, 3, 4, sep=' ')
print(1, 2, 3, 4, sep='#')
print(1, 2, 3, 4, sep='')
print(1, 2, 3, 4, sep='', end='\n')  # '换行'
print(1, 2, 3, 4, sep='', end='!!!')
print("aaaaaaaaaaaaaaa", end='\n\n\n')  # 换三行
print("bbbbbbbbbbb", end='', flush=True)
print('ccccccccccc', end='', flush=True)
print("程序结束")




