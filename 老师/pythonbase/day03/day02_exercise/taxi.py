#   1. 北京出租车计价器:
#     收费标准:
#       3公里以内收费13元
#       基本单价 2.3元/公里(超出3公里以外)
#       空驶费: 超过15公里后,每公里加收单价的50%空驶
#             费(3.45元/公里)
#     要求:
#       输入公里数,打印出费用的金额(以元为单位,精确到分,分以后
#       四舍五入)

km = float(input("请输入公里数: "))
pay = 0  # pay 代表要支付的钱数 
if 0 <= km <= 3:
    pay = 13
elif 3 < km <= 15:
    pay = 13 + (km - 3) * 2.3
elif km > 15:
    pay = 13 + (km - 3) * 2.3 + (km - 15) * 2.3 * .5

print("您需要支付:", round(pay, 2), '元')
